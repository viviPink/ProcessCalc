

def check(lst):
    """
    Проверяет, упорядочены ли элементы списка по убыванию.
    :param lst: Список чисел
    :return: True, если элементы упорядочены по убыванию, иначе False
    """
    for i in range(len(lst) - 1):
        if lst[i] < lst[i + 1]:
            return False
    return True


def double(lst):
    """
    Удваивает все элементы списка.
    :param lst: Список чисел
    :return: Список с удвоенными значениями
    """
    return [i * 2 for i in lst]


def aBs(lst):
    """
    Заменяет все элементы списка на их абсолютные значения.
    :param lst: Список чисел
    :return: Список с абсолютными значениями
    """
    return [abs(i) for i in lst]


def same_color(k, l, m, n):
    """
    Функция проверяет, являются ли два поля шахматной доски полями одного цвета.

    Параметры:
    (вертикаль, горизонталь)
        k, l: координаты первого поля
        m, n: координаты второго поля

    Возвращает:
        True, если поля одного цвета
    """
    # Вычисляем суммы координат для обоих полей
    sum1 = k + l
    sum2 = m + n
    # Проверяем четность сумм
    return sum1 % 2 == sum2 % 2
